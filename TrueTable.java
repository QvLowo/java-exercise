public class TrueTable {
    public static void main(String[] args) {
        int And = 253 & 134; // 1111 1101 ,1000 0110 電腦運算時會轉換為二進制
        System.out.println(And);
        int Or = 253 | 134;
        System.out.println(Or);
        int Xor = 253 ^ 134;
        System.out.println(Xor);
    }
}

